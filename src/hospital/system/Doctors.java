    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        
        if(!name.getText().isEmpty() && !age.getText().isEmpty()){
            try {
                String txt_Name = name.getText();
                double txt_age = Double.parseDouble(age.getText());
                String gender;
                if (male.isSelected()) {
                    gender = "Male";
                } else {
                    gender = "Female";
                }
                
                
                String deptName = cbox.getSelectedItem().toString();
                
                
                PreparedStatement stmt = con.prepareStatement("insert into doctor(name,age,gender,dep) VALUES (?,?,?,?)");
                
                stmt.setString(1, txt_Name);
                stmt.setDouble(2, txt_age);
                stmt.setString(3, gender);
                stmt.setString(4, deptName);
                
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Doctor Add Successfully");
                filltablemodel();

            } catch (SQLException ex) {
                Logger.getLogger(Doctors.class.getName()).log(Level.SEVERE, null, ex);
            }
        } 
    }//GEN-LAST:event_addActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
                
                               
                
        try {
            PreparedStatement stmt = con.prepareStatement("delete from doctor where doctor_id=?");
            
            stmt.setInt(1, doctorId.get(tbl.getSelectedRow()));
            
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Doctor Deleted Successfully");
            filltablemodel();
        } catch (SQLException ex) {
            Logger.getLogger(Doctors.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_delActionPerformed

